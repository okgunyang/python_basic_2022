# 인덱스 및 슬라이싱
name = "kim gi tae is 1004"       # 변수명[begin:end]
print("name[0] =", name[0])        # 인덱스가 0인 데이터
print("name[2:6] =",name[2:6])     # 인덱스가 2부터 6전 까지의 문자열
print("len(name) =", len(name))     # len(변수) : 해당 변수의 글자길이
print("name[2:] =", name[2:])       # end값이 생략되면 끝까지
print("name[:6] =", name[:6])       # begin값이 생략되면 처음
print("name[::2] =", name[::2])     # 인덱스가 짝수 번째인 값
print("name[-2:] =", name[-2:])     # 값이 음수이면, 오른쪽 부터
